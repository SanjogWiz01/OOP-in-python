'''Pythons built-in functions like len() and max() are polymorphic they work with 
different data types and return results based on type of object passed. This showcases it's dynamic nature,
where same function name adapts its behavior depending on input.'''
print(len("Hello sanjog "))  # String length
print(len([1, 2, 3,4]))  # List length array way

print(max(1, 3, 2))  # Maximum of integers
print(max("a", "z", "m"))  # Maximum in strings lexicographically